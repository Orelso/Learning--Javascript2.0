/* Other mathematical assignment operators include the increment operator (++) and 
decrement operator (--).The increment operator will increase the value of the variable 
by 1. The decrement operator will decrease the value of the variable by 1. */

let counter = 10;
counter++;
console.log(counter) // 11

let counterDec = 10;
counterDec--;
console.log(counterDec) //9
